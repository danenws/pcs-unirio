    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        contoBuilder c = new contoBuilder(); //Cria um objeto do tipo conto
        Font font = new Font("Serif", Font.ITALIC, 20);
        jTextPane1.setFont(font);
        String [] x;
        x = c.lerConto("contos/conclusao/tls.txt"); //Lê o conteúdo do conto em um arquivo e armazena no vetor x
       
        int tam = 0; 
        try {
            tam = c.tamanhoConto("contos/conclusao/tls.txt"); //Pega o tamanho do vetor do conteúdo do conto
        } catch (FileNotFoundException ex) {
            Logger.getLogger(TheAnt.class.getName()).log(Level.SEVERE, null, ex);
        }
        //System.out.println(tam);
        
        if(cont<tam-1){
             if (cont==2){
             if (dica== 1){
                 cont++;
            jTextPane1.setText(x[cont]);

             } else {
             JOptionPane.showMessageDialog(null, "Você tem uma dica te esperando!!");
             }}
              else {
            cont++;
            jTextPane1.setText(x[cont]);

             
        }}else{
        
            TheLiony quiz = new TheLiony(nomeUsuario, titulo);
            quiz.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton2MouseClicked

